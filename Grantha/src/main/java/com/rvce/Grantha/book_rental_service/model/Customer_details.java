package com.rvce.Grantha.book_rental_service.model;

import jakarta.persistence.*;

@Entity
@Table(name = "Customer_details")
public class Customer_details {
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;
    private String name;
    private String email;
    private String phone;
    private String password;
    private String role;  // "CUSTOMER" or "SUPPLIER"

    // One-to-One relationship with Customer_address
    @OneToOne(mappedBy = "customerDetails") // Customer_address owns the relationship
    private Customer_address customerAddress;

    public Customer_details() {
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }
}
